# Loops are iteration of sequence

# Demonstrating for loop
# Printing 0 to 9
for i in range(10):
    print("Looping..", i)

# printing characters of given string
char_Name = "Bharat"
for j in char_Name: # understand like this j = char_Name[0...9<using index>]
    print(j)

